package DAY2;

/*
 * In the example below, we created the num1 and num2 variables as same in the TypeCastingWithError.java example. 
 * We store the sum of both numbers in the sum variable of double type. In the output, we can observe the sum of double type.
 */
public class TypeCastingCorrect {
    public static void main(String[] args) {
        // Define int variables
        int num1 = 5004;
        double num2 = 2.5;
        double sum = num1 + num2;
        // show output
        System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum);
     }
}
